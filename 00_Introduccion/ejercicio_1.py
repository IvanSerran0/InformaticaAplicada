# Introducción
# Una expresión puede ser una operación aritmetica

print(20 + 5)                       # 25
print(500 - 350)                       #150 
print(5 * 6)                       # 30
print(20 / 2)                       # 10.0
print(3 ** 4)                      # 81
print(100 % 22)                       #12 
print(5 // 12)                      # 0
print(7<5)                        # False
print(35.2 * 2 * 9.5)               # 668.8000000000001
print(10j + 5 + 6j + 4)             # 9+16j
print('Hola mundo')                # Hola mundo
print(['Sol', 2, 5])               # ['Sol', 2, 5]
print({'Nombre':'Ivan'})      # {'Nombre': 'Ivan'}
print({'Holaa', 85.7, 91.2})         # {'Holaa', 91.2, 85.7}
print(('FPS', 8.6, 6.4, 10))        # ('FPS', 8.6, 6.4, 10)

# Tipos de datos

print(type(15<6))                       # <class 'bool'>
print(type(2.5 * 1.5 * 3))              # <class 'float'>
print(type(2j + 2 + 3j + 5))            # <class 'complex'>
print(type('Hola mundo'))               # <class 'str'>
print(type(['Sol', 2, 5]))            # <class 'list'>
print(type({'Nombre':'Ivan'}))     #  <class 'dict'>
print(type({'Holaa', 85.7, 91.2}))        # <class 'set'>
print(type(('FPS', 8.6, 6.4, 10)))       # <class 'tuple'>
